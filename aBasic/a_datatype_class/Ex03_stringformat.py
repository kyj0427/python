#       성능(속도)는 %이 더 빠르다고는 하지만 코드가 깔끔하게 하는 것이 더 중요하다고 하고는
#       어느 것으로 선택하라고는 안 나옴

# format()이용
msg = '{}님 오늘도 행복하세요 - {}로부터'
print( msg.format('용','대한민국'))


msg = '{0}님 오늘도 행복하세요 - {1}로부터' # {숫자를 많이 생략한다}
print( msg.format('용','대한민국'))

msg = '{1}님 오늘도 행복하세요 - {0}로부터' # 파라미터가 0,1 이기떄문에 순서가 바뀌어서 들어간다
print( msg.format('용','대한민국'))

msg = '{name}님 오늘도 행복하세요 - {group}로부터'
print( msg.format(name='용', group='대한민국'))
