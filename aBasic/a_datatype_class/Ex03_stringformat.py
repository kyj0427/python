# 실수인 경우
#
su = 99.99
print('내가 좋아하는 숫자는 ', su , '이다')
print('내가 좋아하는 숫자는 %0.2f이다' % su)
print('내가 좋아하는 숫자는 %.2f이다' % su) # .앞에 정수부분이 0이면 생략해도 된다
print('내가 좋아하는 숫자는 {0:.1f}이다'.format(su))
# 앞에있는건 실수 , 뒤에있는건 소숫점 n번째 자리수
# 자리수 지정시 반올림이 되기떄문에 주의해줘야한다

su = 99.9999
print('내가 좋아하는 숫자는 ', su , '이다')
print('내가 좋아하는 숫자는 %0.2f이다' % su)
print('내가 좋아하는 숫자는 %.2f이다' % su)
print('내가 좋아하는 숫자는 {0:.1f}이다'.format(su))
